<p> El proceso es muy simple y solo tomará unos minutos iniciar el trámite.</p>
<p>1.<font color="#06c">	<b>Solicitud</b>.</font> Para solicitar el cambio, por favor comunícate con nosotros vía correo electrónico o por teléfono, detallando el motivo de la devolución del producto:  </p>
<p><b>•	Cambio por producto defectuoso</b>: una devolución por producto defectuoso es aquella que se produce porque el producto tiene un defecto de fabricación. En ningún caso se aceptará una devolución de productos que hayan sido usados, siempre que no vaya más allá de la mera comprobación del buen estado y funcionamiento del mismo o que estén en un mal estado como consecuencia de su uso.</p>


